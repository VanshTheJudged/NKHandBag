/* Helper — lets the material filter work even on older products that don't
   have `materialType` set explicitly, by inferring it from `materials`.     */
export function getMaterialType(product: Product): MaterialType | null {
  if (product.materialType) return product.materialType;
  const m = product.materials.join(' ').toLowerCase();
  if (m.includes('jute')) return 'Jute';
  if (m.includes('non-woven') || m.includes('non woven') || m.includes('polypropylene')) return 'Non-woven';
  if (m.includes('canvas')) return 'Canvas';
  if (m.includes('woven')) return 'Woven';
  if (m.includes('meti')) return 'Meti';
  if (m.includes('rexine')) return 'Rexine';
  if (m.includes('faux leather') || m.includes('pu)') || m.includes('chrome') || m.includes('pvc') || m.includes('nylon') || m.includes('polyester')) return 'Synthetic';
  return null;
}
